# Quiz: Average Electricity Bill
# It's time to try a calculation in Python!

# My electricity bills for the last three months have been $23, 
# $32 and $64. What is the average monthly electricity bill over
# the three month period? Write an expression to calculate the mean, 
# and use print() to view the result.

# Answer
